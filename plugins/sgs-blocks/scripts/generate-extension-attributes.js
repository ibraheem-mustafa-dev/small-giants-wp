/**
 * generate-extension-attributes.js
 *
 * Single source of truth for the cross-block `sgs*` editor-extension attributes.
 *
 * The SGS editor extensions (animation, responsive-visibility, conditional-
 * visibility, hover-effects, parallax, custom-css, image-controls, block-link,
 * click-effect) add `sgs*` attributes to blocks CLIENT-SIDE via
 * `addFilter('blocks.registerBlockType', ...)`. Blocks that preview through
 * `ServerSideRender` send those attributes to the WP-core block-renderer REST
 * route, which validates against the SERVER-registered attribute schema with
 * `additionalProperties => false`. If an attribute is not registered
 * server-side, the route rejects the whole request with
 * "Invalid parameter(s): attributes".
 *
 * This script scans the extension JS — the authoritative definition of those
 * attributes — and emits `includes/extension-attributes.generated.php`, which
 * the `register_block_type_args` filter consumes to mirror the schema onto the
 * server. It runs in WRITE mode automatically on `npm run build` /
 * `npm run start` (prebuild / prestart), so any build regenerates the file from
 * the JS and cannot ship a stale list. The `--check` mode is provided for
 * wiring into a pre-commit hook or CI when one exists; it is NOT currently
 * wired into any automated step (qc-council Rater C, 2026-06-08).
 *
 * Usage:
 *   node scripts/generate-extension-attributes.js          # write the PHP file
 *   node scripts/generate-extension-attributes.js --check  # exit 1 if stale (for CI/pre-commit)
 */

'use strict';

const fs = require( 'fs' );
const path = require( 'path' );

const EXT_DIR = path.join( __dirname, '..', 'src', 'blocks', 'extensions' );
const OUT_FILE = path.join(
	__dirname,
	'..',
	'includes',
	'extension-attributes.generated.php'
);

// Matches an `sgsName: { ... }` attribute-definition object literal and
// captures the name + the object body. `type` is then extracted from the body
// independent of key order and quote style (single OR double quotes), so a
// `prettier` quote-style change or a `{ default: '', type: 'string' }` ordering
// cannot silently drop an attribute (qc-council Rater C, 2026-06-08). Attribute
// objects never contain nested braces, so `[^}]*` safely captures the body.
const ATTR_RE = /(sgs[A-Za-z0-9]+)\s*:\s*\{([^}]*)\}/g;
const TYPE_RE = /\btype\s*:\s*["'](string|number|boolean|array|object)["']/;

function collectAttributes() {
	const files = fs
		.readdirSync( EXT_DIR )
		.filter( ( f ) => f.endsWith( '.js' ) )
		.sort();

	const map = new Map();
	const conflicts = [];

	for ( const file of files ) {
		const src = fs.readFileSync( path.join( EXT_DIR, file ), 'utf8' );
		let m;
		while ( ( m = ATTR_RE.exec( src ) ) !== null ) {
			const [ , name, body ] = m;
			const typeMatch = body.match( TYPE_RE );
			if ( ! typeMatch ) {
				// An sgs* object literal with no recognised `type` key — skip,
				// but it is almost certainly a definition that needs a type.
				continue;
			}
			const type = typeMatch[ 1 ];
			if ( map.has( name ) && map.get( name ) !== type ) {
				conflicts.push(
					`${ name }: ${ map.get( name ) } vs ${ type } (${ file })`
				);
			}
			if ( ! map.has( name ) ) {
				map.set( name, type );
			}
		}
	}

	if ( conflicts.length ) {
		throw new Error(
			'Conflicting sgs* attribute types across extensions:\n  ' +
				conflicts.join( '\n  ' )
		);
	}

	// Deterministic order for clean diffs.
	return new Map( [ ...map.entries() ].sort( ( a, b ) => a[ 0 ].localeCompare( b[ 0 ] ) ) );
}

function renderPhp( attrs ) {
	const lines = [];
	lines.push( '<?php' );
	lines.push( '/**' );
	lines.push(
		' * AUTO-GENERATED by scripts/generate-extension-attributes.js — DO NOT EDIT BY HAND.'
	);
	lines.push(
		' * Regenerate with `npm run build` (or `node scripts/generate-extension-attributes.js`).'
	);
	lines.push(
		' * Single source of truth = the `sgs*` attribute definitions in src/blocks/extensions/*.js.'
	);
	lines.push( ' *' );
	lines.push( ' * @package SGS\\Blocks' );
	lines.push( ' */' );
	lines.push( '' );
	lines.push( 'return array(' );
	for ( const [ name, type ] of attrs ) {
		lines.push( `\t'${ name }' => array( 'type' => '${ type }' ),` );
	}
	lines.push( ');' );
	lines.push( '' );
	return lines.join( '\n' );
}

function main() {
	const check = process.argv.includes( '--check' );
	const attrs = collectAttributes();
	const php = renderPhp( attrs );

	if ( check ) {
		const current = fs.existsSync( OUT_FILE )
			? fs.readFileSync( OUT_FILE, 'utf8' )
			: '';
		if ( current !== php ) {
			process.stderr.write(
				'[generate-extension-attributes] STALE: includes/extension-attributes.generated.php ' +
					'is out of date with src/blocks/extensions/*.js. Run `npm run build`.\n'
			);
			process.exit( 1 );
		}
		process.stdout.write(
			`[generate-extension-attributes] OK — ${ attrs.size } attributes, up to date.\n`
		);
		return;
	}

	fs.writeFileSync( OUT_FILE, php );
	process.stdout.write(
		`[generate-extension-attributes] wrote ${ attrs.size } attributes to includes/extension-attributes.generated.php\n`
	);
}

main();
